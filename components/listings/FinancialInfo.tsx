    "The total price the seller is asking for the business, including tangible and intangible assets.",
  annualRevenue:
    "Gross revenue generated by the business over the most recent 12-month period, before any expenses are deducted.",
  cashFlowSDE:
    "Seller's Discretionary Earnings — total financial benefit to a single owner-operator, including salary, perks, and non-cash expenses added back.",
  netIncome:
    "The business's profit after all operating expenses, taxes, and interest have been deducted from revenue.",
  profitMargin:
    "Net income as a percentage of annual revenue. A higher margin indicates greater profitability.",
  askingMultiple:
    "Asking price divided by annual cash flow (SDE). Lower multiples may indicate better value relative to earnings.",
  monthlyRent:
    "The monthly lease payment for the business premises. Important for understanding fixed overhead costs.",
  rentEscalation:
    "How rent increases over time — typically expressed as a percentage per year or a fixed dollar amount.",
  annualPayroll:
    "Total yearly cost of employee wages, salaries, and related benefits (excluding owner compensation).",
  totalExpenses:
    "All annual operating expenses combined, including rent, payroll, supplies, and other overhead.",
  inventoryValue:
    "The estimated value of physical inventory (products, materials, supplies) at the time of sale.",
  ffeValue:
    "Furniture, Fixtures & Equipment — the value of physical assets such as tables, shelving, kitchen equipment, and machinery.",
  sellerFinancing:
    "When the seller agrees to finance part of the purchase price, allowing the buyer to pay over time instead of all at closing.",
