// ---------------------------------------------------------------------------
// Sub-components
// ---------------------------------------------------------------------------

function InfoTooltip({ text }: { text: string }) {
  return (
    <Tooltip>
      <TooltipTrigger asChild>
        <button
          type="button"
          className="inline-flex items-center justify-center rounded-full text-muted-foreground/60 hover:text-muted-foreground transition-colors duration-150 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring"
          aria-label="More information"
        >
          <Info className="h-3.5 w-3.5" strokeWidth={2} />
        </button>
      </TooltipTrigger>
      <TooltipContent side="top" sideOffset={6} className="max-w-64 text-pretty">
        {text}
      </TooltipContent>
    </Tooltip>
  );
}

interface RowProps {
  label: string;
  tooltip: string;
  value: React.ReactNode;
  even: boolean;
}

function Row({ label, tooltip, value, even }: RowProps) {
  return (
    <div
      className={`flex items-center justify-between gap-4 px-4 py-3 ${
        even ? "bg-muted/40" : ""
      }`}
    >
      <div className="flex items-center gap-2 min-w-0">
        <span className="text-sm font-medium text-foreground/80 whitespace-nowrap">
          {label}
        </span>
        <InfoTooltip text={tooltip} />
      </div>
      <div className="text-sm font-semibold text-foreground text-right shrink-0">
        {value}
      </div>
    </div>
  );
}

function NotDisclosed() {
  return (
    <span className="text-muted-foreground/60 font-normal italic">
      Not disclosed
    </span>
  );
}

function AvailableBadge({ available }: { available: boolean }) {
  if (available) {
    return (
      <Badge className="bg-emerald-50 text-emerald-700 border-emerald-200 dark:bg-emerald-950/40 dark:text-emerald-400 dark:border-emerald-800 font-medium">
        Available
      </Badge>
    );
  }
  return (
    <Badge
      variant="secondary"
      className="bg-muted text-muted-foreground border-border font-medium"
    >
      Not available
    </Badge>
  );
}

function IncludedBadge({ included }: { included: boolean | null | undefined }) {
  if (included == null) return null;
  if (included) {
    return (
      <Badge className="bg-emerald-50 text-emerald-700 border-emerald-200 dark:bg-emerald-950/40 dark:text-emerald-400 dark:border-emerald-800 font-medium text-[11px]">
        Included
      </Badge>
    );
  }
  return (
    <Badge
      variant="secondary"
      className="bg-muted text-muted-foreground border-border font-medium text-[11px]"
    >
      Not included
    </Badge>
  );
}

// ---------------------------------------------------------------------------
// Main Component
// ---------------------------------------------------------------------------

export function FinancialInfo({ listing }: FinancialInfoProps) {
  const askingPrice = toNumber(listing.askingPrice);
  const annualRevenue = toNumber(listing.annualRevenue);
  const cashFlowSDE = toNumber(listing.cashFlowSDE);
  const netIncome = toNumber(listing.netIncome);
  const profitMargin = toNumber(listing.profitMargin);
  const askingMultiple = toNumber(listing.askingMultiple);
  const monthlyRent = toNumber(listing.monthlyRent);
  const annualPayroll = toNumber(listing.annualPayroll);
  const totalExpenses = toNumber(listing.totalExpenses);
  const inventoryValue = toNumber(listing.inventoryValue);
  const ffeValue = toNumber(listing.ffeValue);

  // Build rows — each entry: [label, tooltipKey, rendered value]
  const rows: [string, string, React.ReactNode][] = [
    [
      "Asking Price",
      "askingPrice",
      askingPrice != null ? formatCurrency(askingPrice) : <NotDisclosed />,
    ],
    [
      "Annual Revenue",
      "annualRevenue",
      annualRevenue != null ? formatCurrency(annualRevenue) : <NotDisclosed />,
    ],
    [
      "Cash Flow (SDE)",
      "cashFlowSDE",
      cashFlowSDE != null ? formatCurrency(cashFlowSDE) : <NotDisclosed />,
    ],
    [
      "Net Income",
      "netIncome",
      netIncome != null ? formatCurrency(netIncome) : <NotDisclosed />,
    ],
    [
      "Profit Margin",
      "profitMargin",
      profitMargin != null ? (
        `${Number(profitMargin.toFixed(1))}%`
      ) : (
        <NotDisclosed />
      ),
    ],
    [
      "Asking Multiple",
      "askingMultiple",
      askingMultiple != null ? (
        `${Number(askingMultiple.toFixed(1))}x`
      ) : (
        <NotDisclosed />
      ),
    ],
    [
      "Monthly Rent",
      "monthlyRent",
      monthlyRent != null ? formatCurrency(monthlyRent) : <NotDisclosed />,
    ],
    [
      "Rent Escalation",
      "rentEscalation",
      listing.rentEscalation ?? <NotDisclosed />,
    ],
    [
      "Annual Payroll",
      "annualPayroll",
      annualPayroll != null ? formatCurrency(annualPayroll) : <NotDisclosed />,
    ],
    [
      "Total Expenses",
      "totalExpenses",
      totalExpenses != null ? formatCurrency(totalExpenses) : <NotDisclosed />,
    ],
    [
      "Inventory",
      "inventoryValue",
      inventoryValue != null ? (
        <span className="inline-flex items-center gap-2">
          {formatCurrency(inventoryValue)}
          <IncludedBadge included={listing.inventoryIncluded} />
        </span>
      ) : (
        <NotDisclosed />
      ),
    ],
    [
      "FF&E",
      "ffeValue",
      ffeValue != null ? (
        <span className="inline-flex items-center gap-2">
          {formatCurrency(ffeValue)}
          <IncludedBadge included={listing.ffeIncluded} />
        </span>
      ) : (
        <NotDisclosed />
      ),
    ],
    [
      "Seller Financing",
      "sellerFinancing",
      <AvailableBadge key="sf" available={listing.sellerFinancing} />,
    ],
    [
      "SBA Financing",
      "sbaFinancingAvailable",
      <AvailableBadge key="sba" available={listing.sbaFinancingAvailable} />,
    ],
  ];

  return (
    <TooltipProvider delayDuration={200}>
      <Card className="overflow-hidden">
        <CardHeader className="border-b pb-4">
          <CardTitle className="text-lg font-semibold tracking-tight">
            Financial Information
          </CardTitle>
        </CardHeader>
        <CardContent className="p-0">
          <div className="divide-y divide-border/50">
            {rows.map(([label, tooltipKey, value], index) => (
              <Row
                key={tooltipKey}
                label={label}
                tooltip={TOOLTIPS[tooltipKey]}
                value={value}
                even={index % 2 === 0}
              />
            ))}
          </div>
        </CardContent>
      </Card>
    </TooltipProvider>
  );
}
